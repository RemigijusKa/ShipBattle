package zaidimas.Data;

public class Game {
    public static final String READY_FOR_SECOND_PLAYER = "READY_FOR_SECOND_PLAYER";
    public static final String READY_FOR_SHIPS = "READY_FOR_SHIPS";
    public static final String READY_TO_PLAY = "READY_TO_PLAY";
    public static final String FINISHED = "FINISHED";
    public static final String COLUMN_NAMES = "KILOMETRAS";


    String status;
    String gameId;
    String nextTurnId;
    String winnerId;


    public Game(String gameId, String status) {
        this.gameId = gameId;
        this.status = status;
    }

    public static String getReadyForSecondPlayer() {
        return READY_FOR_SECOND_PLAYER;
    }

    public static String getReadyForShips() {
        return READY_FOR_SHIPS;
    }

    public static String getReadyToPlay() {
        return READY_TO_PLAY;
    }

    public static String getFINISHED() {
        return FINISHED;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getGameId() {
        return gameId;
    }

    public void setGameId(String gameId) {
        this.gameId = gameId;
    }

    public String getNextTurnId() {
        return nextTurnId;
    }

    public void setNextTurnId(String nextTurnId) {
        this.nextTurnId = nextTurnId;
    }

    public String getWinnerId() {
        return winnerId;
    }

    public void setWinnerId(String winnerId) {
        this.winnerId = winnerId;
    }
}

